                        <h4>Eko-okullar programı nedir?</h4>

                        <p>
                            Eko-Okullar Programı okul öncesi ile ilk ve ortaokullarda çevre bilinci, çevre yönetimi ve sürdürülebilir kalkınma eğitimi vermek için uygulanan bir programdır. Katılımcı yaklaşımıyla okullardaki öğrenciler hem çevresel konularda bilgi edinirler, hem de ailelerini, yerel yönetimleri ve sivil toplum kuruluşlarını (STK) çevresel konularda bilinçlendirmede etkin rol alırlar. Program,okullarda <strong>ISO 14001/EMAS</strong> üzerine kurulmuş bir çevre yönetim sisteminin uygulanmasını da sağlar.
                            <br><br>
                            Yerel düzeyde sürdürülebilir kalkınma sürecinin uygulanmasına yardımcı olmak üzere, öğrenciler okulun çevresel etkilerini azaltmak için uygulanan 7 adım‘da etkin rol almak için yönlendirilirler. Dolayısıyla eko-okullar, sınıfta ders öğretmenin ötesine geçerek, toplumun diğer bölümlerinde de çevre duyarlılığının sağlanmasında rol almış olurlar.
                            <br><br>
                            Program, çevre için yapılan bütünsel bir okul faaliyetini kapsar ve uygulandığı okullardaki başarısı, okul müdürü başta olmak üzere okul idaresinin ve öğretmenlerinin ilgisine bağlıdır. Eko-Okullar programındaki en önemli ve bütünleştirici etken ise öğrenci katılımıdır. Komitenin yerel halkı ve yöneticileri bilinçlendirme çabaları ise öğrencilerde diyalog kurabilme becerilerini geliştirmeyi ve iyi bir yurttaşlık eğitimini sağlar.
                        </p>

                        <br>

                        <h4>PROGRAMIN FAYDALARI</h4>
                        <p>Programın uygulanması, öğrencilere çevre konusunda olduğu kadar, yaşamları boyunca kullanacakları ve başarılarını etkileyecek alışkanlıklar kazandırır.</p>

                        <br>

                        <h4>ÖĞRENCİLER;</h4>
                        <ol class="arrow">
                            <li>– Bir grup üyesidir ve yeni bir kimlik geliştirir,</li>
                            <li>– Grup çalışmasına alışır katılımcı bir yapı oluşturur,</li>
                            <li>– Sorunları tanıma, çözüm üretme, ve tartışma becerisi geliştirir,</li>
                            <li>– İnisiyatif kullanma, karar verme yeteneği gelişir,</li>
                            <li>– Plan yapma, rapor yazma becerisi gelişir,</li>
                            <li>– Tüketim alışkanlıkları değişir, savurganlık önlenir,</li>
                            <li>– Doğal kaynakları koruma bilinci gelişir.</li>
                        </ol>

                        <br>
                        <p>Bunların yanında programın <strong>OKULA</strong> faydaları şöyledir;</p>

                        <ol class="arrow">
                            <li>– Temizlik ve düzenini sürekli kılar,</li>
                            <li>– Su ve elektrik tasarrufu öğrenci eliyle sağlanır,</li>
                            <li>– Okul öğrencilerce sahiplenilir,</li>
                            <li>– Okul, bulunduğu bölgenin merkezi durumundadır,</li>
                            <li>– Yerel, ulusal, hatta uluslararası boyutta tanınır,</li>
                            <li>– İsterse iletişim ağımız kanalıyla ulusal ve uluslararası düzeyde etkileşir.</li>
                        </ol>
