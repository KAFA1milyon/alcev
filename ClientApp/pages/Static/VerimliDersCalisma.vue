                        <h4><strong>Matematik dersi nasıl çalışılmalı?</strong></h4>
                        <p>Matematik dersinde başarıyı arttırmanın yolu, her derste olduğu gibi okuma alışkanlığına bağlıdır. Matematik dersinde başarınızı arttırmak için, dersi iyi dinlemeli, derse aktif olarak katılmalı, evde, işlenen konularla ilgili soru çözmelisiniz. Bu dersde başarılı olabilmek için bol bol pratik yapmak gerekir.</p>

                        <ol class="arrow">
                            <li>
                                Derste anlayamadığınız soruları o derste sormalısınız.
                            </li>
                            <li>
                                Alıştırma sorularını, sınırda çözülen soruları,çözümlü soru örneklerini tekrar çözüp,konuyla ilgili problem çözümüne geçmelisiniz.
                            </li>
                            <li>
                                İşlemleri zihinden yapmaya çalışmak basit sorularda dahi hata yapılmasına sebep olabilir.Bunu engellemek için işlemleri yazarak yapmalısınız.
                            </li>
                            <li>
                                Soruları gerektiğinde şema, grafik gibi şekillerle ifade etmek çözümlerini daha iyi görmenizi sağlayacaktır.
                            </li>
                            <li>
                                Evde yaptığınız çalışmalarda çözemediğiniz soruları mutlaka öğretmenlerinize sormalısınız.
